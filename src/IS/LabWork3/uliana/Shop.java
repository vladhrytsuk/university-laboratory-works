package IS.LabWork3.uliana;

public class Shop {

    private String shopName;
    private Double area;

    public Shop(String shopName, Double area) {
        this.shopName = shopName;
        this.area = area;
    }

    public String getShopName() {
        return shopName;
    }

    public Double getArea() {
        return area;
    }
}
